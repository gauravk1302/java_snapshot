public class Literals {
    public static void main(String[] args) {
        int a = 123;
        int b = 0146;
        int c = 0X73b;
        int d = 0b1101;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
    float e = 101.230f;
    float f = 0123.222f;
}
